import { BIRTH } from "../constants";

export const red = (n) => {
  while (n > 9) n = String(n).split("").reduce((a, c) => a + Number(c), 0);
  return n;
};

export function personalDay(iso) {
  const [y, m, d] = iso.split("-").map(Number);
  const py = red(BIRTH.day + BIRTH.month + red(String(y).split("").reduce((a, c) => a + Number(c), 0)));
  const pm = red(py + m);
  return { py, pm, pd: red(pm + d) };
}

